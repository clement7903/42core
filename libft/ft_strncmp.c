#include "libft.h"

int	ft_strncmp(const char *str1, const char *str2, size_t n)
{
	while (n > 0)
	{
		if (*str1 != *str2)
		{
			return (*str1 - *str2);
		}
		if (*str1 == '\0')
		{
			return (0);
		}
		str1++;
		str2++;
		n--;
	}
	return (0);
}
